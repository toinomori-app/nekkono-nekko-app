let currentPhase = "material";
let currentSeedIndex = 0;

const materialAnswers = materialSeeds.map((seed) =>
  Array(seed.items.length).fill(null)
);

const meguriAnswers = meguriSeeds.map((seed) =>
  Array(seed.items.length).fill(null)
);

const startScreen =
  document.getElementById("start-screen");

const checkScreen =
  document.getElementById("check-screen");

const resultScreen =
  document.getElementById("result-screen");

const startButton =
  document.getElementById("start-button");

const backButton =
  document.getElementById("back-button");

const nextButton =
  document.getElementById("next-button");

const restartButton =
  document.getElementById("restart-button");

const seedNumber =
  document.getElementById("seed-number");

const seedTitle =
  document.getElementById("seed-title");

const progressText =
  document.getElementById("progress-text");

const progressFill =
  document.getElementById("progress-fill");

const questions =
  document.getElementById("questions");

const errorMessage =
  document.getElementById("error-message");

const resultContent =
  document.getElementById("result-content");

function getCurrentSeeds() {
  return currentPhase === "material"
    ? materialSeeds
    : meguriSeeds;
}

function getCurrentAnswers() {
  return currentPhase === "material"
    ? materialAnswers
    : meguriAnswers;
}

function showScreen(screen) {
  document
    .querySelectorAll(".screen")
    .forEach((item) => {
      item.classList.remove("active");
    });

  screen.classList.add("active");

  window.scrollTo({
    top: 0,
    behavior: "smooth"
  });
}

function renderSeed() {
  const seeds = getCurrentSeeds();
  const answers = getCurrentAnswers();
  const seed = seeds[currentSeedIndex];

  const phaseName =
    currentPhase === "material"
      ? "素材のタネ"
      : "巡りのタネ";

  seedNumber.textContent =
    `${phaseName} ${seed.number}`;

  seedTitle.textContent =
    `${seed.icon} ${seed.title}`;

  progressText.textContent =
    `${currentSeedIndex + 1} / ${seeds.length}`;

  progressFill.style.width =
    `${((currentSeedIndex + 1) / seeds.length) * 100}%`;

  backButton.style.visibility =
    currentSeedIndex === 0 &&
    currentPhase === "material"
      ? "hidden"
      : "visible";

  const isLastSeed =
    currentSeedIndex === seeds.length - 1;

  if (
    currentPhase === "material" &&
    isLastSeed
  ) {
    nextButton.textContent =
      "巡りのチェックへ";
  } else if (
    currentPhase === "meguri" &&
    isLastSeed
  ) {
    nextButton.textContent =
      "結果を見る";
  } else {
    nextButton.textContent =
      "次へ";
  }

  questions.innerHTML = "";
  errorMessage.textContent = "";

  seed.items.forEach(
    (item, questionIndex) => {
      const card =
        document.createElement("div");

      card.className =
        "question-card";

      const title =
        document.createElement("p");

      title.className =
        "question-title";

      title.textContent =
        item;

      const choiceGroup =
        document.createElement("div");

      choiceGroup.className =
        "choice-group";

      choices.forEach((choice) => {
        const label =
          document.createElement("label");

        const input =
          document.createElement("input");

        input.type =
          "radio";

        input.name =
          `${currentPhase}-${currentSeedIndex}-${questionIndex}`;

        input.value =
          choice.value;

        input.checked =
          answers[currentSeedIndex][questionIndex] ===
          choice.value;

        input.addEventListener(
          "change",
          () => {
            answers[currentSeedIndex][questionIndex] =
              choice.value;

            errorMessage.textContent =
              "";
          }
        );

        const symbol =
          document.createElement("span");

        symbol.textContent =
          choice.label;

        const text =
          document.createElement("small");

        text.textContent =
          choice.text;

        label.appendChild(input);
        label.appendChild(symbol);
        label.appendChild(text);

        choiceGroup.appendChild(label);
      });

      card.appendChild(title);
      card.appendChild(choiceGroup);

      questions.appendChild(card);
    }
  );
}

function isCurrentSeedComplete() {
  const answers =
    getCurrentAnswers();

  return answers[currentSeedIndex].every(
    (answer) => answer !== null
  );
}

function calculateResults(
  seeds,
  answers
) {
  return seeds.map(
    (seed, index) => {
      const count4 =
        answers[index].filter(
          (answer) =>
            answer === 4
        ).length;

      const count5 =
        answers[index].filter(
          (answer) =>
            answer === 5
        ).length;

      return {
        ...seed,
        count4,
        count5,
        total: count4 + count5
      };
    }
  );
}

function getWinningSeeds(results) {
  const highestTotal =
    Math.max(
      ...results.map(
        (result) =>
          result.total
      )
    );

  const totalWinners =
    results.filter(
      (result) =>
        result.total ===
        highestTotal
    );

  const highestCount5 =
    Math.max(
      ...totalWinners.map(
        (result) =>
          result.count5
      )
    );

  return totalWinners.filter(
    (result) =>
      result.count5 ===
      highestCount5
  );
}

function getCardImagePath(
  winner,
  type
) {
  if (type === "material") {
    const number =
      String(winner.key)
        .padStart(2, "0");

    return `images/jyuunin-${number}.png`;
  }

  const number =
    meguriImageNumbers[winner.key];

  return `images/meguri-${number}.png`;
}

function makeWinnerCards(
  winners,
  type,
  typeLabel
) {
  let html = "";

  winners.forEach((winner) => {
    const imagePath =
      getCardImagePath(
        winner,
        type
      );

    html += `
      <article class="result-card">

        <p class="result-label">
          ${typeLabel}
        </p>

        <h3>
          ${winner.number}
          ${winner.title}
        </h3>

        <img
          class="result-card-image"
          src="${imagePath}"
          alt="${winner.title}のカード"
        >

        <details class="score-details">

          <summary>
            集計を見る
          </summary>

          <p>
            ❹の数：
            ${winner.count4}
          </p>

          <p>
            ❺の数：
            ${winner.count5}
          </p>

          <p>
            ❹と❺の合計：
            ${winner.total}
          </p>

        </details>

      </article>
    `;
  });

  if (winners.length === 2) {
    html += `
      <p class="tie-message">
        どちらのタネも<br>
        持ち合わせているようです🌱
      </p>
    `;
  }

  if (winners.length >= 3) {
    html += `
      <p class="tie-message">
        いずれのタネも<br>
        持ち合わせているようです🌱
      </p>
    `;
  }

  return html;
}

function showResults() {
  const materialResults =
    calculateResults(
      materialSeeds,
      materialAnswers
    );

  const meguriResults =
    calculateResults(
      meguriSeeds,
      meguriAnswers
    );

  const materialWinners =
    getWinningSeeds(
      materialResults
    );

  const meguriWinners =
    getWinningSeeds(
      meguriResults
    );

  const resultHeading =
    resultScreen.querySelector("h2");

  if (resultHeading) {
    resultHeading.textContent =
      "🌱 今の巡りあわせ";
  }

  resultContent.innerHTML = `
    <section class="result-section">

      <h2>
        🌳 今の住人
      </h2>

      ${makeWinnerCards(
        materialWinners,
        "material",
        "今出やすい素材のタネ"
      )}

    </section>

    <div class="result-cross">
      ×
    </div>

    <section class="result-section">

      <h2>
        🪽 今の巡り屋
      </h2>

      ${makeWinnerCards(
        meguriWinners,
        "meguri",
        "今出やすい巡りのタネ"
      )}

    </section>

    <p class="result-footer">
      今日のあなたの<br>
      ねっこを知るヒントにしてね🌱
    </p>
  `;

  showScreen(resultScreen);
}

startButton.addEventListener(
  "click",
  () => {
    currentPhase = "material";
    currentSeedIndex = 0;

    renderSeed();
    showScreen(checkScreen);
  }
);

backButton.addEventListener(
  "click",
  () => {
    if (currentSeedIndex > 0) {
      currentSeedIndex -= 1;

      renderSeed();

      return;
    }

    if (
      currentPhase === "meguri" &&
      currentSeedIndex === 0
    ) {
      currentPhase =
        "material";

      currentSeedIndex =
        materialSeeds.length - 1;

      renderSeed();
    }
  }
);

nextButton.addEventListener(
  "click",
  () => {
    if (!isCurrentSeedComplete()) {
      errorMessage.textContent =
        "すべての項目を選んでから進んでね🌱";

      return;
    }

    const seeds =
      getCurrentSeeds();

    const isLastSeed =
      currentSeedIndex ===
      seeds.length - 1;

    if (!isLastSeed) {
      currentSeedIndex += 1;

      renderSeed();

      window.scrollTo({
        top: 0,
        behavior: "smooth"
      });

      return;
    }

    if (
      currentPhase === "material"
    ) {
      currentPhase =
        "meguri";

      currentSeedIndex =
        0;

      renderSeed();

      window.scrollTo({
        top: 0,
        behavior: "smooth"
      });

      return;
    }

    showResults();
  }
);

restartButton.addEventListener(
  "click",
  () => {
    materialAnswers.forEach(
      (seedAnswers) => {
        seedAnswers.fill(null);
      }
    );

    meguriAnswers.forEach(
      (seedAnswers) => {
        seedAnswers.fill(null);
      }
    );

    currentPhase =
      "material";

    currentSeedIndex =
      0;

    const resultHeading =
      resultScreen.querySelector("h2");

    if (resultHeading) {
      resultHeading.textContent =
        "🌱 今出やすい素材のタネ";
    }

    showScreen(startScreen);
  }
);
